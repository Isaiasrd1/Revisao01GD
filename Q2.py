# 2. Explique a função map()

# A função map() é uma função do Python que aplica uma função 
# a cada elemento de um iterável e retorna um novo iterável com os resultados. 
# Ela recebe como parâmetro uma função e um ou mais iteráveis e retorna
# um objeto do tipo map, que é um iterador que produz os resultados da função
# aplicada a cada elemento dos iteráveis de entrada.
# A função 'map ()' é bastante útil em situações em que é necessário aplicar 
# uma mesma operação a vários elementos de uma lista, dicionário, 
# ou qualquer outro tipo de iterável.  Permitindo, assim, que o código seja mais 
# conciso e legível, evitando repetições.

